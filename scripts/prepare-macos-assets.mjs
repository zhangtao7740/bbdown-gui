import fs from 'node:fs/promises'
import fsSync from 'node:fs'
import path from 'node:path'
import { spawnSync } from 'node:child_process'

const projectRoot = process.cwd()
const requireUniversal = process.argv.includes('--require-universal')
const sourceBBDownArm64 = process.env.BBDOWN_ARM64_BIN_PATH || process.env.BBDOWN_BIN_PATH || '/Users/zhangtao/Downloads/BBDown'
const sourceBBDownX64 = process.env.BBDOWN_X64_BIN_PATH || '/Users/zhangtao/Downloads/BBDown-osx-x64/BBDown'
const toolsDir = path.join(projectRoot, 'build', 'tools')
const legacyTargetBBDown = path.join(toolsDir, 'BBDown')
const archTargets = {
  arm64: path.join(toolsDir, 'darwin-arm64', 'BBDown'),
  x64: path.join(toolsDir, 'darwin-x64', 'BBDown'),
}

function run(command, args) {
  return spawnSync(command, args, { encoding: 'utf-8' })
}

function commandPath(command) {
  const result = run('which', [command])
  return result.status === 0 ? result.stdout.trim() : ''
}

async function normalizeExecutable(filePath) {
  await fs.chmod(filePath, 0o755)
  if (process.platform === 'darwin') {
    run('xattr', ['-dr', 'com.apple.quarantine', filePath])
  }
}

function detectMachOArchs(filePath) {
  const result = run('file', [filePath])
  const output = result.status === 0 ? result.stdout.trim() : ''
  const archs = new Set()
  if (/\barm64\b/.test(output)) archs.add('arm64')
  if (/\bx86_64\b/.test(output)) archs.add('x64')
  return { archs, output }
}

async function copyBBDownForArch(arch, sourcePath) {
  if (!sourcePath || !fsSync.existsSync(sourcePath)) {
    return { arch, copied: false, sourcePath, reason: 'missing' }
  }

  await normalizeExecutable(sourcePath)
  const info = detectMachOArchs(sourcePath)
  if (info.archs.size > 0 && !info.archs.has(arch)) {
    throw new Error(`BBDown ${arch} source has incompatible architecture: ${info.output || sourcePath}`)
  }

  const targetPath = archTargets[arch]
  await fs.mkdir(path.dirname(targetPath), { recursive: true })
  await fs.copyFile(sourcePath, targetPath)
  await normalizeExecutable(targetPath)
  return { arch, copied: true, sourcePath, targetPath, info }
}

await fs.mkdir(toolsDir, { recursive: true })
await fs.writeFile(
  path.join(toolsDir, 'README.txt'),
  [
    'This directory is generated by npm run build:mac-assets.',
    'BBDown arm64 is copied from BBDOWN_ARM64_BIN_PATH, BBDOWN_BIN_PATH, or /Users/zhangtao/Downloads/BBDown.',
    'BBDown x64 is copied from BBDOWN_X64_BIN_PATH or /Users/zhangtao/Downloads/BBDown-osx-x64/BBDown.',
    'Universal macOS builds require both build/tools/darwin-arm64/BBDown and build/tools/darwin-x64/BBDown.',
    'FFmpeg and FFprobe are intentionally not bundled; install them with Homebrew on macOS.',
    '',
  ].join('\n')
)

if (process.platform !== 'darwin') {
  console.log('macOS asset preparation skipped because this is not darwin.')
  process.exit(0)
}

const copiedTools = [
  await copyBBDownForArch('arm64', sourceBBDownArm64),
  await copyBBDownForArch('x64', sourceBBDownX64),
]

const arm64Tool = copiedTools.find((item) => item.arch === 'arm64')
const x64Tool = copiedTools.find((item) => item.arch === 'x64')
if (!arm64Tool?.copied) {
  throw new Error(`BBDown arm64 binary not found: ${sourceBBDownArm64}`)
}

if (requireUniversal && !x64Tool?.copied) {
  throw new Error(
    `Universal macOS build requires BBDown x64. Download BBDown_1.6.3_20240814_osx-x64.zip and set BBDOWN_X64_BIN_PATH, or place it at ${sourceBBDownX64}.`
  )
}

const legacySource = process.arch === 'x64' && x64Tool?.copied ? x64Tool.targetPath : arm64Tool.targetPath
await fs.copyFile(legacySource, legacyTargetBBDown)
await normalizeExecutable(legacyTargetBBDown)

for (const tool of copiedTools) {
  if (tool.copied) {
    console.log(`BBDown ${tool.arch}: ${tool.info.output}`)
  } else {
    console.warn(`BBDown ${tool.arch}: not found at ${tool.sourcePath}`)
  }
}

const ffmpegPath = commandPath('ffmpeg')
const ffprobePath = commandPath('ffprobe')
if (!ffmpegPath || !ffprobePath) {
  console.warn('FFmpeg/FFprobe not found on PATH. Install with: brew install ffmpeg')
} else {
  console.log(`ffmpeg: ${ffmpegPath}`)
  console.log(`ffprobe: ${ffprobePath}`)
}
